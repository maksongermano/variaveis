let x = 10;
let y = 2;
// lembre de deixar os espaços entre as aspas e os sinais para poder aparecer os espaçamento indicado no programa.
let resultado = x + y;

console.log(`A soma de ${x} e ${y} é ${resultado} conforme-se com isso`);//a sooma de 10 e 2 é 12

resultado = x - y;
console.log(`A subitração de ${x} e ${y} é igual a ${resultado}`)// aqui é para ele exibir o 8, mas ainda preciso aprender como inserir a virgula no código sem quebrar o comando


resultado = x * y;//20 mutiplicação

console.log(`A mutiplicação entre ${x} e ${y} é ${resultado} pois é isso`)//ainda precisando aprender a por virgulas nesse tipo de código

resultado = x / y;//divisão 5

console.log(`O resultado da divisão entre ${x} e ${y} é o produto numérico ${resultado} `);// a variavel criada com Crase ` é primordial para podermos incluir outros elementos dentro da linha de comando, fazemos isso com $ e {} em seguida; 